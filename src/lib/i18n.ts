export type Language = 'zh' | 'en'
export type TranslationKey = keyof typeof translations.zh

export function getTranslation(lang: Language, key: string): string {
  const keys = key.split('.')
  let value: any = translations[lang]
  
  for (const k of keys) {
    value = value?.[k]
  }
  
  return value || key
}